# 위치 알려주기

a = "Hello, my name is HwangSangBin"
print(a.find("e"))
print(a.find("m"))
print(a.find("k"))
# find는 찾고자 하는 것이 없으면 -1 출력
print(a.index("H"))
#print(a.index("k"))
# index는 찾고자 하는 것이 없으면 에러로 프로그램 종료
b = "Hello"
print(b.upper())
# 대문자로 출력
print(b.lower())
# 소문자로 출력
c = "Life is Girl"
print(c.replace("Life", "Live"))
# 문자 교체
print(c.split())
d = "You:are:very:good:job"
print(d.split(":"))
